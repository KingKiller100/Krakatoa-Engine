#include "pch.hpp"
#include "CalenderExceptions.hpp"

namespace klib::kDebug
{
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Calendar Exception  Base //////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	CalendarError::CalendarError()
		: runtime_error("Calendar Error")
	{}

	CalendarError::CalendarError(const std::string& _Message)
		: runtime_error(_Message)
	{}

	CalendarError::CalendarError(const char* const _Message)
		: runtime_error(_Message)
	{}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Invalid Day Error /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	InvalidDayError::InvalidDayError()
		: CalendarError("Invalid Day Of The Week Error")
	{
	}
	InvalidDayError::InvalidDayError(const std::string& _Message)
		: CalendarError(_Message)
	{}

	InvalidDayError::InvalidDayError(const char* const _Message)
		: CalendarError(_Message)
	{}

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// Invalid Day Error /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	InvalidMonthError::InvalidMonthError()
		: CalendarError("Invalid Month Of The Year Error")
	{}

	InvalidMonthError::InvalidMonthError(const std::string& _Message)
		: CalendarError(_Message)
	{}

	InvalidMonthError::InvalidMonthError(const char* const _Message)
		: CalendarError(_Message)
	{}
}
